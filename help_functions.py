from django.template.defaultfilters import slugify as django_slugify

alphabet = {'а': 'a', 'б': 'b', 'в': 'v', 'г': 'g', 'д': 'd', 'е': 'e', 'ё': 'yo', 'ж': 'zh', 'з': 'z', 'и': 'i',
            'й': 'j', 'к': 'k', 'л': 'l', 'м': 'm', 'н': 'n', 'о': 'o', 'п': 'p', 'р': 'r', 'с': 's', 'т': 't',
            'у': 'u', 'ф': 'f', 'х': 'kh', 'ц': 'ts', 'ч': 'ch', 'ш': 'sh', 'щ': 'shch', 'ы': 'i', 'э': 'e', 'ю': 'yu',
            'я': 'ya'}

def python_slugify(s: str):
    return django_slugify('новости'.join(alphabet.get(w, w) for w in s.lower()))

def python_slugify_list(lst: list[str]):
    returned_lst = []
    for item in lst:
        returned_lst.append(django_slugify('новости'.join(alphabet.get(w, w) for w in item.lower())))
    return returned_lst
